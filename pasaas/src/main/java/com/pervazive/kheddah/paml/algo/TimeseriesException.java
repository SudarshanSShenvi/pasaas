package com.pervazive.kheddah.paml.algo;

public class TimeseriesException extends Exception {
	private static final long serialVersionUID = 1;

	public TimeseriesException() {
	}

	public TimeseriesException(String arg0) {
		super(arg0);
	}

	public TimeseriesException(Throwable arg0) {
		super(arg0);
	}

	public TimeseriesException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
